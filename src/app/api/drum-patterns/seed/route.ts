export async function POST() {
  try {
    const db = getDb()

    // Check if already seeded
    const count = db.prepare('SELECT COUNT(*) as count FROM drum_patterns').get() as any
    if (count.count > 0) {
      return NextResponse.json({ message: 'Já existe dados', count: count.count })
    }

    // Insert seed data
    const insert = db.prepare(
      'INSERT INTO drum_patterns (nome, bpm, kit, steps) VALUES (?, ?, ?, ?)'
    )

    for (const pattern of SEED_PATTERNS) {
      insert.run(pattern.nome, pattern.bpm, pattern.kit, JSON.stringify(pattern.steps))
    }

    return NextResponse.json({ 
      message: 'Seed realizado com sucesso', 
      count: SEED_PATTERNS.length 
    })
  } catch (error) {
    console.error('[drum-patterns seed POST]', error)
    return NextResponse.json({ error: 'Erro ao executar seed' }, { status: 500 })
  }
}
